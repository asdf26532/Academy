package model.vo;

/*
	IDX  NOT NULL NUMBER       
	NAME NOT NULL VARCHAR2(30) 
	KOR           NUMBER       
	ENG           NUMBER       
	MAT           NUMBER
*/

public class StudentVO {
	private int idx;
	private String name;
	private int kor, eng, mat;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getKor() {
		return kor;
	}
	public void setKor(int kor) {
		this.kor = kor;
	}
	public int getEng() {
		return eng;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public int getMat() {
		return mat;
	}
	public void setMat(int mat) {
		this.mat = mat;
	}
}
